# Membership Operators:
# in Returs True if a value is found in the sequence
# not in Returs True if a value is not found in the sequence

list = [3,4,12,2,]
print(12 in list)  # the answer is always be True or False. it'd true beacuse we found the element in the list
print (24 in list)

print(0 not in list) 